# 5. Copy a model to another resource
# ---------------------------------------------------------------------------
def copy_model_to_resource(source_model_id: str, target_endpoint: str, target_key: str):
    """
    Copy a model to another Azure Document Intelligence resource.
    Useful for promoting models from dev → staging → production.

    Steps:
      1. Get a copy authorization from the TARGET resource
      2. Initiate the copy from the SOURCE resource using the authorization
    """
    print(f"\n{'='*60}")
    print(f" Copy Model: '{source_model_id}' → {target_endpoint}")
    print(f"{'='*60}")

    try:
        # Step 1: Authorize the copy in the target resource
        target_admin = DocumentIntelligenceAdministrationClient(
            endpoint=target_endpoint,
            credential=AzureKeyCredential(target_key),
        )
        copy_auth = target_admin.authorize_model_copy(
            authorize_copy_request={"modelId": f"{source_model_id}-copy"}
        )
        print(f"  Copy authorization obtained. Target model: {copy_auth.model_id}")

        # Step 2: Initiate copy from source resource
        poller = admin_client.begin_copy_document_model_to(
            model_id=source_model_id,
            copy_to_request=copy_auth,
        )
        result = poller.result()
        print(f"  Model copied successfully! Target model ID: {result.model_id}")

    except HttpResponseError as e:
        print(f"  Copy error: {e.message}")


# ---------------------------------------------------------------------------
# 6. Delete a model (cleanup)
# ---------------------------------------------------------------------------
def delete_model(model_id: str):
    """
    Delete a custom or composed model. This is irreversible.
    Use when retiring old models or managing quota.
    """
    print(f"\nDeleting model '{model_id}'...")
    try:
        admin_client.delete_document_model(model_id)
        print(f"  Model '{model_id}' deleted.")
    except HttpResponseError as e:
        if e.status_code == 404:
            print(f"  Model '{model_id}' not found (already deleted?).")
        else:
            print(f"  Error: {e.message}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
def main():
    print("Azure Document Intelligence — Custom Model Lifecycle Demo")
    print(f"Endpoint: {ENDPOINT}")

    try:
        # 1. Show current model inventory
        print("\n--- Existing Models ---")
        models = list(admin_client.list_models())
        print(f"Total models: {len(models)}")
        for m in models[:10]:
            print(f"  {m.model_id}")

        # 2. Train a custom model (only if SAS URL is provided)
        if TRAINING_SAS_URL:
            build_custom_model(
                model_id=CUSTOM_MODEL_ID,
                sas_url=TRAINING_SAS_URL,
                build_mode="neural",
                description="AI-102 demo: custom invoice model",
            )
        else:
            print("\n[SKIP] Training: AZURE_TRAINING_CONTAINER_SAS_URL not set.")

        # 3. Get details of the custom model
        get_model_details(CUSTOM_MODEL_ID)

        # 4. Test the model on a sample document
        # Use the sample invoice from the prebuilt demo
        SAMPLE_DOC_URL = (
            "https://raw.githubusercontent.com/Azure-Samples/"
            "cognitive-services-REST-api-samples/master/curl/form-recognizer/sample-invoice.pdf"
        )
        test_custom_model(CUSTOM_MODEL_ID, SAMPLE_DOC_URL)

        # 5. Compose two custom models into one
        compose_models(
            component_model_ids=[CUSTOM_MODEL_ID, CUSTOM_MODEL_ID_2],
            composed_model_id=COMPOSED_MODEL_ID,
        )

        # 6. Test the composed model
        get_model_details(COMPOSED_MODEL_ID)

        print("\nCustom model lifecycle demo complete!")
        print("\nExam Tips:")
        print("  - Custom neural models handle layout variations better than template")
        print("  - Need 5+ labeled samples minimum; 10+ recommended for good accuracy")
        print("  - Confidence scores < 0.8 suggest the field needs more training samples")
        print("  - Composed models classify the document type automatically")
        print("  - Models can be copied between regions for DR or deployment pipelines")

    except KeyError as e:
        print(f"Missing environment variable: {e}")
        raise
    except HttpResponseError as e:
        print(f"API error [{e.status_code}]: {e.message}")
        raise


if __name__ == "__main__":
    main()
